const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('fhk10-lang') || 'en')
  useEffect(() => {
    localStorage.setItem('fhk10-lang', lang)
    document.documentElement.lang = lang
  }, [lang])
  const t = (key) => (STRINGS[lang] && STRINGS[lang][key]) || STRINGS.en[key] || key
  const toggle = () => setLang((l) => (l === 'en' ? 'ru' : 'en'))
  return <I18nContext.Provider value={{ lang, setLang, toggle, t }}>{children}</I18nContext.Provider>
}

export const useI18n = () => useContext(I18nContext)
